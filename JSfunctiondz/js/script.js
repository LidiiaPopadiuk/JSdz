  let arrStr = string.split(" ");
  let longestWord;
  let wordLength = 0;
  for (let i = 0; i < arrStr.length; i += 1) {
    wordLength = arrStr[1].length;
    if (arrStr[i].length > wordLength) {
      longestWord = arrStr[i];
      return longestWord;
    }
  }
